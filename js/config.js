function config($stateProvider, $urlRouterProvider, $ocLazyLoadProvider) {
    $urlRouterProvider.otherwise("/index/private_profile");

    $ocLazyLoadProvider.config({
        // Set to true if you want to see what and when is dynamically loaded
        debug: false
    });

    $stateProvider

        .state('index', {
            abstract: true,
            url: "/index",
            templateUrl: "views/common/content.html",
        })
        .state('index.main', {
            url: "/main",
            templateUrl: "views/main.html",
            data: { pageTitle: 'Example view' }
        })
        .state('index.minor', {
            url: "/minor",
            templateUrl: "views/minor.html",
            data: { pageTitle: 'Example view' }
        }).state('index.private_profile', {
            url: "/private_profile",
            templateUrl: "views/private_profile.html",
            data: { pageTitle: "Private Profile" }
        }).state('index.public_profile', {
            url: "/public_profile",
            templateUrl: "views/public_profile.html",
            data: { pageTitle: "public profile" }
        }).state('index.create_ou', {
            url: "/create_ou",
            templateUrl: "views/create_ou.html",
            data: { pageTitle: "create_ou" }
        })
}
angular
    .module('inspinia')
    .config(config)
    .run(function($rootScope, $state) {
        $rootScope.$state = $state;
    });
